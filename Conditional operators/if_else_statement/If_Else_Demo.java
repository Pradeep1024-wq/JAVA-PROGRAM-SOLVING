public class If_Else_Demo {

	public static void main(String[] args) 
	{
		int time = 1;
    	if (time < 10) 
    	{
    	  System.out.println("Good morning.");
    	} else if (time < 20) 
    	{
    	  System.out.println("Good day.");
    	} 
    	else 
    	{
    	  System.out.println("Good evening.");
    	}
	}
}
